function getSource() {
	return this.source;
}

function setSource(country) {
	this.source = country;
}

function getDest() {
	return this.dest;
}

function setDest(country) {
	this.dest = country;
}

function getFlow() {
	return this.flow;
}

function setFlow(num) {
	this.flow = num;
}

function getCapacity() {
	return this.capacity;
}

function setCapacity(num) {
	this.capacity = num;
}